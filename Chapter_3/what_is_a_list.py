# A list is a collection of items in a particular order. You can make a list that includes the letters of the alphabet,
# the digits from 0 to 9, or the names of all the people in your family.
# You can put anything you want into a list, and the items in your list don’t have to be related in any particular way.
# Because a list usually contains more than one element, it’s a good idea to make the name of your list plural,
# such as "letters", "digits", or "names".

# In Python, square brackets ([]) indicate a list, and individual elements in the list are separated by commas.
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
# If you ask Python to print a list, Python returns its representation of the list, including the square brackets.
print(bicycles)
