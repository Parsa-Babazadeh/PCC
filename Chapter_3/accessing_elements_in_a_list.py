# Lists are ordered collections, so you can access any element in a list by telling Python the position, or index,
# of the item desired. To access an element in a list,
# write the name of the list followed by the index of the item enclosed in square brackets.

bicycles = ['trek', 'cannondale', 'redline', 'specialized']
# When we ask for a single item from a list, Python returns just that element without square brackets:
print(bicycles[0])

# You can format the element 'trek' to look more presentable by using the title() method:
print(bicycles[0].title())
