# You can add (+), subtract (-), multiply (*), and divide (/) integers in Python.
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
# In a terminal session, Python simply returns the result of the operation.
# Python uses two multiplication symbols to represent exponents:
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)
# Python supports the order of operations too, so you can use multiple operations in one expression.
# You can also use parentheses to modify the order of operations;
# so Python can evaluate your expression in the order you specify. For example:
print(2 + 3*4)
print((2 + 3) * 4)
