# You can assign values to more than one variable using just a single line of code.
# This can help shorten your programs and make them easier to read;
# you’ll use this technique most often when initializing a set of numbers.
# For example, here’s how you can initialize the variables x, y, and z to zero:
x, y, z, = 0, 0, 0
# You need to separate the variable names with commas, and do the same with the values,
# and Python will assign each value to its respective variable.
# As long as the number of values matches the number of variables, Python will match them up correctly.
print(x, y, z)
